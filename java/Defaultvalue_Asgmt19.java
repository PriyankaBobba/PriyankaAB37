/*19.WAP to find out default values of byte,short ,int, long,double,float,boolean,char 
and string data types using global variable */

package assignments;

public class Defaultvalue_Asgmt19 
{
	static byte a;
	static short b;
	static int c;
	static long d;
	static double e;
	static float f;
	static boolean g;
	static char h;
	static String i;
	
	public static void main(String[] args) 
	{
		System.out.println("byte default value:"+ a);
		System.out.println("short default value:"+ b);
		System.out.println("int default value:"+ c);
		System.out.println("long default value:"+ d);
		System.out.println("double default value:"+ e);
		System.out.println("float default value:"+ f);
		System.out.println("boolean default value:"+ g);
		System.out.println("char default value:"+ h);
		System.out.println("string default value:"+ i);
	}
}
